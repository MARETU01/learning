#!/bin/bash
# ============================================
# Git 常用命令 - 日常提交流程
# ============================================

# 查看当前状态（修改了哪些文件）
git status

# 添加指定文件到暂存区
git add <文件名>

# 添加所有修改到暂存区
git add .

# 提交到本地仓库
git commit -m "提交说明"

# 添加并提交（跳过 git add，仅限已跟踪文件）
git commit -am "提交说明"

# 推送到远程仓库
git push origin <分支名>

# 拉取远程最新代码（fetch + merge）
git pull origin <分支名>
