#!/bin/bash
# ============================================
# Git 常用命令 - 标签管理
# ============================================

# --- 创建标签 ---
git tag <标签名>                    # 轻量标签
git tag -a <标签名> -m "说明"       # 附注标签（推荐）

# --- 查看标签 ---
git tag

# --- 推送标签到远程 ---
git push origin <标签名>            # 推送单个标签
git push origin --tags              # 推送所有标签

# --- 删除标签 ---
git tag -d <标签名>                 # 删除本地标签
git push origin :refs/tags/<标签名>  # 删除远程标签
