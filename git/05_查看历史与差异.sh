#!/bin/bash
# ============================================
# Git 常用命令 - 查看历史与差异
# ============================================

# --- 查看提交历史 ---
git log
git log --oneline          # 简洁模式（一行一条）
git log --oneline --graph  # 图形化显示分支合并

# --- 查看某个文件的修改历史 ---
git log -p <文件名>

# --- 查看差异 ---
git diff                   # 工作区 vs 暂存区
git diff --cached          # 暂存区 vs 最新提交
git diff <分支1> <分支2>    # 两个分支的差异
