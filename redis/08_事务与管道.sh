#!/bin/bash
# ============================================
# Redis 常用命令 - 事务与管道
# ============================================

# ============================================
# 一、事务（Transaction）
# ============================================
# Redis事务特点：
# 1. 命令按顺序执行，不会被其他客户端命令插入
# 2. 不支持回滚（与MySQL事务不同！）
# 3. 某条命令执行失败不影响其他命令

# --- 基本事务 ---
MULTI                                  # 开启事务
SET account:A 1000                     # 命令入队（返回QUEUED）
SET account:B 2000                     # 命令入队
DECRBY account:A 100                   # A减100
INCRBY account:B 100                   # B加100
EXEC                                   # 执行事务中所有命令

# --- 放弃事务 ---
MULTI
SET key1 "value1"
SET key2 "value2"
DISCARD                                # 放弃事务，所有入队命令被丢弃

# --- 乐观锁（WATCH） ---
# WATCH用于实现CAS（Check-And-Set）操作
# 如果被WATCH的key在EXEC之前被其他客户端修改，事务将失败
WATCH account:A                        # 监视key
GET account:A                          # 读取当前值
MULTI                                  # 开启事务
DECRBY account:A 100                   # 修改
EXEC                                   # 如果account:A被其他客户端修改，返回nil
UNWATCH                                # 取消所有WATCH

# ============================================
# 二、管道（Pipeline）
# ============================================
# 管道不是Redis命令，而是客户端技术
# 将多个命令一次性发送给服务器，减少网络往返(RTT)
# 注意：管道不是原子操作，不保证事务性

# --- redis-cli 管道示例 ---
# 方式1：通过管道符
echo -e "SET key1 value1\nSET key2 value2\nGET key1\nGET key2" | redis-cli --pipe

# 方式2：从文件读取命令
# 创建命令文件 commands.txt：
#   SET user:1 "张三"
#   SET user:2 "李四"
#   SET user:3 "王五"
cat commands.txt | redis-cli --pipe

# 方式3：批量导入数据（Redis协议格式）
# 生成Redis协议格式的数据文件，然后通过管道导入
# 格式：*参数数量\r\n$参数长度\r\n参数值\r\n...

# ============================================
# 三、事务 vs 管道 vs Lua脚本 对比
# ============================================
# | 特性       | 事务(MULTI/EXEC) | 管道(Pipeline) | Lua脚本(EVAL) |
# |-----------|-----------------|---------------|--------------|
# | 原子性     | 是（不可中断）    | 否             | 是            |
# | 回滚       | 不支持           | 不支持          | 不支持（但可自行处理） |
# | 条件逻辑   | 不支持           | 不支持          | 支持          |
# | 减少RTT    | 是              | 是             | 是            |
# | 乐观锁     | WATCH支持        | 不支持          | 不需要（天然原子） |
# | 适用场景   | 简单批量操作      | 批量读写        | 复杂原子操作   |
